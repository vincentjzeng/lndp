package com.lndp.model;

public class Contact {

	private String companyName;
	
	private String companyTel;
	
	private String companyFax;
	
	private String companyAddress;
	
	private String companyPost;
	
	private String clientServiceQQ;
	
	private String companyDeveloper;
	
	private String companyEmail;
	
	private String companyWebsite;

	public String getCompanyName() {
		return companyName;
	}

	public void setCompanyName(String companyName) {
		this.companyName = companyName;
	}

	public String getCompanyTel() {
		return companyTel;
	}

	public void setCompanyTel(String companyTel) {
		this.companyTel = companyTel;
	}

	public String getCompanyFax() {
		return companyFax;
	}

	public void setCompanyFax(String companyFax) {
		this.companyFax = companyFax;
	}

	public String getCompanyAddress() {
		return companyAddress;
	}

	public void setCompanyAddress(String companyAddress) {
		this.companyAddress = companyAddress;
	}

	public String getCompanyPost() {
		return companyPost;
	}

	public void setCompanyPost(String companyPost) {
		this.companyPost = companyPost;
	}

	public String getClientServiceQQ() {
		return clientServiceQQ;
	}

	public void setClientServiceQQ(String clientServiceQQ) {
		this.clientServiceQQ = clientServiceQQ;
	}

	public String getCompanyDeveloper() {
		return companyDeveloper;
	}

	public void setCompanyDeveloper(String companyDeveloper) {
		this.companyDeveloper = companyDeveloper;
	}

	public String getCompanyEmail() {
		return companyEmail;
	}

	public void setCompanyEmail(String companyEmail) {
		this.companyEmail = companyEmail;
	}

	public String getCompanyWebsite() {
		return companyWebsite;
	}

	public void setCompanyWebsite(String companyWebsite) {
		this.companyWebsite = companyWebsite;
	}
	
	
	
	
}
